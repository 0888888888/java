

public class Basic {
    public static void main(String[] args) {
        System.out.println((5 & 6));  //101 & 110 = 100
        System.out.println((5 | 6));  //101 & 110 = 111
        System.out.println((5 ^ 6));  //101 ^ 110 = 011
        System.out.println(~5);
        System.out.println(5<<2);
    }
}
